package com.model.BusinessClass;

import java.util.Date;

public class BusinessClass {
	private int id;
	private int businessClassName;
	private int userId;
	private Date build;
	private String introduce;
	
	
	
	
	
	
	
	
	
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getBusinessClassName() {
		return businessClassName;
	}
	public void setBusinessClassName(int businessClassName) {
		this.businessClassName = businessClassName;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public Date getBuild() {
		return build;
	}
	public void setBuild(Date build) {
		this.build = build;
	}
	public String getIntroduce() {
		return introduce;
	}
	public void setIntroduce(String introduce) {
		this.introduce = introduce;
	}
	
	
	
	
	
	
	
}
